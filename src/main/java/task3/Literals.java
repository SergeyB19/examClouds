public class Literals {
    public static void main(String[] args) {
        System.out.println("Логический литерал " + true);
        System.out.println("Целочисленный литерал " + 011);
        System.out.println("Целочисленный 16-й литерал " + 0xAb11);
        System.out.println("Целочисленный 2-й литерал " + 0b11);
    }
}
